import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const sourceDir =
  process.argv[2] ??
  path.join(repoRoot, "..", "qf-api-docs", "docs", "sdk", "javascript");
const destinationDir =
  process.argv[3] ?? path.join(repoRoot, "apps", "docs", "content", "docs");
const canonicalBaseUrl = "https://api-docs.quran.foundation";
const pageOrder = [
  "index.mdx",
  "runtime-matrix.mdx",
  "auth-matrix.mdx",
  "entrypoint-matrix.mdx",
  "apis-by-runtime.mdx",
  "server-quickstart.mdx",
  "public-quickstart.mdx",
  "full-stack.mdx",
  "search.mdx",
  "chapters.mdx",
  "verses.mdx",
  "audio.mdx",
  "resources.mdx",
  "juzs.mdx",
  "common-errors.mdx",
  "migration-cheat-sheet.mdx",
  "v1-migration-guide.mdx",
];

const parseFrontMatter = (content) => {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) {
    return {};
  }

  const frontMatter = {};
  for (const line of match[1].split(/\r?\n/)) {
    const frontMatterMatch = line.match(/^([A-Za-z_][\w-]*):\s*(.*)$/);
    if (!frontMatterMatch) {
      continue;
    }

    let value = frontMatterMatch[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    frontMatter[frontMatterMatch[1]] = value;
  }

  return frontMatter;
};

const escapeJson = (value) => JSON.stringify(value, null, 2);

const ensureDirectory = (directory) => {
  fs.mkdirSync(directory, { recursive: true });
};

const getCanonicalUrl = (fileName, frontMatter) => {
  if (frontMatter.slug) {
    const slug = frontMatter.slug.startsWith("/")
      ? frontMatter.slug
      : `/${frontMatter.slug}`;
    const normalizedSlug =
      slug === "/docs" || slug.startsWith("/docs/") ? slug : `/docs${slug}`;
    return `${canonicalBaseUrl}${normalizedSlug}`;
  }

  const slug = fileName.replace(/\.mdx?$/, "");
  return `${canonicalBaseUrl}/docs/sdk/javascript/${slug}`;
};

const buildStub = ({ canonicalUrl, description, title }) => {
  return `---
title: ${JSON.stringify(title)}
description: ${JSON.stringify(
    `Thin local mirror for the canonical Quran Foundation SDK docs page: ${title}.`,
  )}
---

Purpose: Local thin docs surface for the canonical Quran Foundation SDK docs.

Use this when: You are browsing the local docs app in the api-js repo.

Do not use this when: You want the full public documentation source of truth.

Backend required: See the canonical page.

Allowed runtimes: See the canonical page.

Required credentials: See the canonical page.

Minimal import: See the canonical page.

This page is generated by \`scripts/sync-sdk-docs.mjs\`.

Canonical page: [${title}](${canonicalUrl})

Summary: ${description || "Read the canonical page for the up-to-date guidance."}

The authored SDK documentation lives in the API docs portal so the SDK repo does not maintain a second manual copy.
`;
};

ensureDirectory(destinationDir);

const files = fs
  .readdirSync(sourceDir)
  .filter((fileName) => fileName.endsWith(".mdx") || fileName.endsWith(".md"))
  .sort((left, right) => {
    const leftIndex = pageOrder.indexOf(left);
    const rightIndex = pageOrder.indexOf(right);

    if (leftIndex !== -1 || rightIndex !== -1) {
      return (
        (leftIndex === -1 ? Number.MAX_SAFE_INTEGER : leftIndex) -
        (rightIndex === -1 ? Number.MAX_SAFE_INTEGER : rightIndex)
      );
    }

    return left.localeCompare(right);
  });

const generatedPages = [];

for (const fileName of files) {
  const sourcePath = path.join(sourceDir, fileName);
  const destinationPath = path.join(destinationDir, fileName);
  const content = fs.readFileSync(sourcePath, "utf8");
  const frontMatter = parseFrontMatter(content);
  const title =
    frontMatter.title ?? fileName.replace(/\.mdx?$/, "").replace(/-/g, " ");
  const description =
    frontMatter.description ?? "Canonical Quran Foundation SDK documentation.";
  const canonicalUrl = getCanonicalUrl(fileName, frontMatter);

  fs.writeFileSync(
    destinationPath,
    buildStub({
      canonicalUrl,
      description,
      title,
    }),
    "utf8",
  );

  generatedPages.push(fileName.replace(/\.mdx?$/, ""));
}

const meta = {
  description: "Thin local mirror for the canonical Quran Foundation SDK docs.",
  icon: "Building",
  pages: ["---Canonical Docs---", ...generatedPages],
  root: true,
  title: "Quran API",
};

fs.writeFileSync(
  path.join(destinationDir, "meta.json"),
  `${escapeJson(meta)}\n`,
  "utf8",
);

console.log(
  `Synced ${generatedPages.length} SDK doc stubs from ${sourceDir} to ${destinationDir}`,
);
